def build_program_html(program):
    items = []
    for item in program:
        if len(item) == 2:
            time, event = item
            if event == "":
                items.append(f'<div class="prog-section">{time}</div>')
            elif time.startswith("→"):
                items.append(f'<div class="prog-sub"><span class="prog-arrow">→</span><span class="prog-ev">{event}</span></div>')
            else:
                items.append(f'<div class="prog-item"><time class="prog-time">{time}</time><span class="prog-ev">{event}</span></div>')
    return "\n      ".join(items)

def build_includes_html(includes):
    return "\n".join([f'<li class="inc-item"><span class="inc-check">✓</span><span>{inc}</span></li>' for inc in includes])

def build_tips_html(tips):
    return "\n".join([f'<li class="tip-item">{tip}</li>' for tip in tips])

