def sample_eggs(n: int = 12) -> str:
    """Случайная горстка пасхалок для подмешивания в промпт (разнообразие без раздувания)."""
    picks = random.sample(EGGS, min(n, len(EGGS)))
    return "\n".join(f"- {e}" for e in picks)
